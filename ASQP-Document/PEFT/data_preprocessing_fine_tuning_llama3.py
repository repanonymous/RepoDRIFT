def quads_list_to_set(quads):
    s = set()
    if not isinstance(quads, (list,tuple)): return s
    for q in quads:
        if isinstance(q, (list,tuple)) and len(q)==4:
            s.add(tuple(str(x).strip().lower() for x in q))
    return s

